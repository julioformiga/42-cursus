int	ft_is_alpha(char *str)
{
	str--;
	while (str++, *str != 0)
		if (!(*(str) >= 'A' && *(str) <= 'Z')
			&& !(*(str) >= 'a' && *(str) <= 'z'))
			return (0);
	return (1);
}
